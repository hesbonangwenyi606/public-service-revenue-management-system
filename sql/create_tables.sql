-- Create TAXPAYERS table
CREATE TABLE TAXPAYERS (
    taxpayer_id VARCHAR2(20) PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    email VARCHAR2(100),
    phone VARCHAR2(20),
    registration_date DATE DEFAULT SYSDATE
);

-- Create REGISTRATION_LOGS table
CREATE TABLE REGISTRATION_LOGS (
    log_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    taxpayer_id VARCHAR2(20) REFERENCES TAXPAYERS(taxpayer_id),
    action VARCHAR2(50),
    log_time TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- Create PAYMENTS table
CREATE TABLE PAYMENTS (
    payment_id VARCHAR2(20) PRIMARY KEY,
    taxpayer_id VARCHAR2(20) REFERENCES TAXPAYERS(taxpayer_id),
    amount NUMBER(10,2) NOT NULL,
    status VARCHAR2(20) NOT NULL,
    method VARCHAR2(20),
    invoice VARCHAR2(30),
    payment_time TIMESTAMP DEFAULT SYSTIMESTAMP
);
